		(או בחיפה) ולאחר-מכן במוסדות-חינוך שונים בתל-אביב. החל להשתתף בכתיבה 
		בעתונות הארץ-ישראלית בגיל צעיר (עורכו הראשון היה י&quot;ח ברנר). פירסם שירים, 
		סיפורים ומאמרים על ספרות וביקורת וסיפורי ילדים, וכן ערך את החוברות 
		לספרות ולמדע לנוער &quot;שערים&quot; (1938), את הקובץ &quot;היה בריא!&quot; (תרצ&quot;ח) ואת 
		הירחון לנוער &quot;מולדת&quot; (1949). שמואל בס נפטר בתל-אביב בג&#39; בסיון תש&quot;ט, 31 
		במאי 1949 ונטמן בבית העלמין בנחלת-יצחק.</p>
